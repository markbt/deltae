use super::*;

fn round4(val: f64) -> f64 {
    let rounded = format!("{:.*}", 4, val);
    rounded.parse().unwrap()
}

fn test_de2000(expected: f64, lab0: &[f64; 3], lab1: &[f64; 3]) {
    let color_0 = LabValue {
        l: lab0[0],
        a: lab0[1],
        b: lab0[2],
    };

    let color_1 = LabValue {
        l: lab1[0],
        a: lab1[1],
        b: lab1[2],
    };

    let de = round4(delta_e_2000(&color_0, &color_1));

    assert_eq!(expected, de);
}

